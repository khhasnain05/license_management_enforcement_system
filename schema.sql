create or replace NONEDITIONABLE PROCEDURE SP_WORKFLOW_REJECT_PAYMENT (
    p_app_id     IN NUMBER,
    p_officer_id IN NUMBER,
    p_reason     IN VARCHAR2
) AS
    v_payment_id   NUMBER;
    v_payment_type VARCHAR2(50);
    v_user_id      NUMBER;
    v_new_id       NUMBER; -- Variable to hold the ID
BEGIN
    -- 1. Find the latest payment
    SELECT payment_id, payment_type INTO v_payment_id, v_payment_type
    FROM PAYMENT WHERE application_id = p_app_id ORDER BY payment_id DESC FETCH FIRST 1 ROWS ONLY;

    -- 2. Update Payment
    UPDATE PAYMENT 
    SET payment_status = 'REJECTED', reviewed_by_officer_id = p_officer_id, remarks = p_reason
    WHERE payment_id = v_payment_id;

    -- 3. Audit Log
    INSERT INTO PAYMENT_AUDIT_LOG (payment_id, officer_id, action_taken, remarks, timestamp)
    VALUES (v_payment_id, p_officer_id, 'REJECTED', p_reason, SYSDATE);

    -- 4. Find User ID
    SELECT a.user_id INTO v_user_id FROM APPLICATION app 
    JOIN APPLICANT a ON app.applicant_id = a.applicant_id WHERE app.application_id = p_app_id;

    -- 5. Update Application Stage
    UPDATE APPLICATION 
    SET current_stage = DECODE(v_payment_type, 'LICENSE_FEE', 'LICENSE_FEE_PENDING', 'LEARNER_FEE_PENDING'), 
        application_status = 'REJECTED', rejection_reason = p_reason
    WHERE application_id = p_app_id;

    -- 6. CRITICAL FIX: Get ID from sequence first, then insert
    SELECT NOTIF_SEQ.NEXTVAL INTO v_new_id FROM DUAL;

    INSERT INTO NOTIFICATION (notification_id, user_id, type, subject, message_body, sent_at, is_read) 
    VALUES (v_new_id, v_user_id, 'PAYMENT_REJECTED', 'Payment Rejected', 'Your payment receipt was rejected. Reason: ' || p_reason, SYSDATE, 0);

    COMMIT;
END;
