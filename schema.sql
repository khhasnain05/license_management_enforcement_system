    v_current_points NUMBER;
BEGIN
    INSERT INTO TRAFFIC_VIOLATION (violation_id, license_id, police_officer_id, challan_no, vehicle_reg_no, violation_type, fine_amount, penalty_points, violation_location, violation_date, challan_status) 
    VALUES (VIOLATION_SEQ.NEXTVAL, p_license_id, p_officer_id, p_challan_no, p_vehicle_reg, p_violation_type, p_fine_amount, p_penalty_points, p_location, SYSDATE, 'PENDING');

