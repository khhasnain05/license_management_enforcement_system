create or replace NONEDITIONABLE PROCEDURE SP_WORKFLOW_VERIFY_PAYMENT (
    p_app_id     IN NUMBER,
    p_officer_id IN NUMBER
) AS
    v_payment_id      NUMBER;
    v_payment_type    VARCHAR2(50);
    v_user_id         NUMBER;
    v_permit_no       VARCHAR2(50);

    v_permit_identity VARCHAR2(3);
    v_notif_identity  VARCHAR2(3);
    v_audit_identity  VARCHAR2(3);

    PROCEDURE add_audit_log (
        p_action  IN VARCHAR2,
        p_remarks IN VARCHAR2
    ) AS
    BEGIN
        IF v_audit_identity = 'YES' THEN
            INSERT INTO PAYMENT_AUDIT_LOG (
                payment_id,
                officer_id,
                action_taken,
                remarks,
                timestamp
            )
            VALUES (
                v_payment_id,
                p_officer_id,
                p_action,
                p_remarks,
                SYSDATE
            );
        ELSE
            INSERT INTO PAYMENT_AUDIT_LOG (
                audit_id,
                payment_id,
                officer_id,
                action_taken,
                remarks,
                timestamp
            )
            VALUES (
                AUDIT_SEQ.NEXTVAL,
                v_payment_id,
                p_officer_id,
                p_action,
                p_remarks,
                SYSDATE
            );
        END IF;
    END add_audit_log;


    PROCEDURE add_notification (
        p_type    IN VARCHAR2,
        p_subject IN VARCHAR2,
        p_message IN VARCHAR2
    ) AS
    BEGIN
        IF v_notif_identity = 'YES' THEN
            INSERT INTO NOTIFICATION (
                user_id,
                type,
                subject,
                message_body,
                sent_at,
                is_read
            )
            VALUES (
                v_user_id,
                p_type,
                p_subject,
                p_message,
                SYSDATE,
                0
            );
        ELSE
            INSERT INTO NOTIFICATION (
                notification_id,
                user_id,
                type,
                subject,
                message_body,
                sent_at,
                is_read
            )
            VALUES (
                NOTIF_SEQ.NEXTVAL,
                v_user_id,
                p_type,
                p_subject,
                p_message,
                SYSDATE,
                0
            );
        END IF;
    END add_notification;

BEGIN
    /*
      Check actual table definitions.
      If ID column is identity, we omit it.
      If ID column is not identity, we insert sequence value.
    */
    SELECT NVL(MAX(identity_column), 'NO')
    INTO v_permit_identity
    FROM user_tab_columns
    WHERE table_name = 'LEARNER_PERMIT'
      AND column_name = 'PERMIT_ID';

    SELECT NVL(MAX(identity_column), 'NO')
    INTO v_notif_identity
    FROM user_tab_columns
    WHERE table_name = 'NOTIFICATION'
      AND column_name = 'NOTIFICATION_ID';

    SELECT NVL(MAX(identity_column), 'NO')
    INTO v_audit_identity
    FROM user_tab_columns
    WHERE table_name = 'PAYMENT_AUDIT_LOG'
      AND column_name = 'AUDIT_ID';


    SELECT payment_id, payment_type, user_id
    INTO v_payment_id, v_payment_type, v_user_id
    FROM PAYMENT
    WHERE application_id = p_app_id
    ORDER BY payment_id DESC
    FETCH FIRST 1 ROWS ONLY;


    UPDATE PAYMENT
    SET payment_status = 'APPROVED',
        reviewed_by_officer_id = p_officer_id,
        remarks = 'Payment Verified by Officer'
    WHERE payment_id = v_payment_id;


    add_audit_log(
        'APPROVED',
        'Payment Verified by Officer'
    );


    IF v_payment_type = 'LICENSE_FEE' THEN

        UPDATE APPLICATION
        SET current_stage = 'LICENSE_GENERATION_PENDING',
            application_status = 'IN_PROGRESS'
        WHERE application_id = p_app_id;

        add_notification(
            'PAYMENT_APPROVED',
            'Payment Approved',
            'Your License Fee is verified.'
        );

    ELSE

        DELETE FROM LEARNER_PERMIT
        WHERE application_id = p_app_id;

        v_permit_no := 'LP-' || TO_CHAR(SYSDATE, 'YYYY') || '-' || p_app_id;

        IF v_permit_identity = 'YES' THEN
            INSERT INTO LEARNER_PERMIT (
                application_id,
                learner_permit_no,
                issue_date,
                expiry_date,
                status
            )
            VALUES (
                p_app_id,
                v_permit_no,
                SYSDATE,
                ADD_MONTHS(SYSDATE, 6),
                'ACTIVE'
            );
        ELSE
            INSERT INTO LEARNER_PERMIT (
                permit_id,
                application_id,
                learner_permit_no,
                issue_date,
                expiry_date,
                status
            )
            VALUES (
                PERMIT_SEQ.NEXTVAL,
                p_app_id,
                v_permit_no,
                SYSDATE,
                ADD_MONTHS(SYSDATE, 6),
                'ACTIVE'
            );
        END IF;


        UPDATE APPLICATION
        SET current_stage = 'LEARNER_ISSUED',
            application_status = 'IN_PROGRESS'
        WHERE application_id = p_app_id;

        add_notification(
            'PERMIT_ISSUED',
            'Permit Issued',
            'Your Learner Permit has been issued.'
        );

    END IF;

    COMMIT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(
            -20002,
            'No payment found for application_id: ' || p_app_id
        );

    WHEN OTHERS THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(
            -20001,
            'Procedure failed: ' || SQLERRM
        );
END;
