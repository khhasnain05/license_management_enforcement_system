create or replace NONEDITIONABLE PROCEDURE SP_UPDATE_PAYMENT_SUBMIT (
    p_app_id         IN NUMBER,
    p_transaction_id IN VARCHAR2,
    p_payment_method IN VARCHAR2
) AS
    v_payment_type VARCHAR2(50);
BEGIN
    -- Update the latest pending payment for this application
    UPDATE PAYMENT 
    SET transaction_id = p_transaction_id, payment_method = p_payment_method, 
        payment_date = SYSDATE, payment_status = 'VERIFICATION_PENDING', remarks = NULL
    WHERE application_id = p_app_id AND payment_status = 'PENDING_CHALLAN'
    RETURNING payment_type INTO v_payment_type;

    -- Move Application Stage forward
    IF v_payment_type = 'LICENSE_FEE' THEN
        UPDATE APPLICATION SET current_stage = 'FINAL_PAYMENT_VERIFICATION' WHERE application_id = p_app_id;
    ELSE
        UPDATE APPLICATION SET current_stage = 'PAYMENT_VERIFICATION' WHERE application_id = p_app_id;
    END IF;

    COMMIT;
END;
