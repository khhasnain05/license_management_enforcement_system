create or replace NONEDITIONABLE PROCEDURE SP_CREATE_LEARNER_PERMIT (
    p_applicant_user_email IN VARCHAR2,
    p_category             IN VARCHAR2,
    p_application_id       OUT NUMBER
) AS
    v_applicant_id NUMBER;
    v_user_id      NUMBER;
    v_challan_no   VARCHAR2(50);
BEGIN
    SELECT a.applicant_id, a.user_id INTO v_applicant_id, v_user_id
    FROM APPLICANT a JOIN DLMS_USER u ON a.user_id = u.user_id WHERE u.email = p_applicant_user_email;

    SELECT APPLICATION_SEQ.NEXTVAL INTO p_application_id FROM DUAL;

    INSERT INTO APPLICATION (application_id, applicant_id, license_category, application_type, application_status, submission_date, current_stage) 
    VALUES (p_application_id, v_applicant_id, p_category, 'NEW', 'PENDING', SYSDATE, 'LEARNER_PAYMENT_PENDING');

    v_challan_no := 'CHL-' || TO_CHAR(SYSDATE, 'YYYY') || '-APP-' || p_application_id;

    INSERT INTO PAYMENT (payment_id, application_id, user_id, challan_no, amount, payment_type, payment_status) 
    VALUES (PAYMENT_SEQ.NEXTVAL, p_application_id, v_user_id, v_challan_no, 300.0, 'LEARNER_FEE', 'PENDING_CHALLAN');
    COMMIT;
END;
/

create or replace NONEDITIONABLE PROCEDURE SP_CREATE_STAFF (
    p_full_name  IN VARCHAR2,
    p_cnic       IN VARCHAR2,
    p_email      IN VARCHAR2,
    p_password   IN VARCHAR2,
    p_role       IN VARCHAR2,
    p_department IN VARCHAR2 DEFAULT NULL,
    p_center     IN VARCHAR2 DEFAULT NULL,
    p_badge      IN VARCHAR2 DEFAULT NULL,
    p_rank       IN VARCHAR2 DEFAULT NULL,
    p_station    IN VARCHAR2 DEFAULT NULL
) AS
    v_user_id NUMBER;
BEGIN
    SELECT USER_SEQ.NEXTVAL INTO v_user_id FROM DUAL;

    INSERT INTO DLMS_USER (user_id, cnic, full_name_en, email, password_hash, role, account_status) 
    VALUES (v_user_id, p_cnic, p_full_name, p_email, p_password, p_role, 'ACTIVE');

    IF p_role = 'LICENSING_OFFICER' THEN
        INSERT INTO LICENSING_OFFICER (officer_id, user_id, department) VALUES (OFFICER_SEQ.NEXTVAL, v_user_id, NVL(p_department, 'General'));
    ELSIF p_role = 'TESTING_OFFICER' THEN
        -- Using identity column for Testing Officer based on previous setup
        INSERT INTO TESTING_OFFICER (user_id, assigned_center) VALUES (v_user_id, NVL(p_center, 'Main Center'));
    ELSIF p_role = 'TRAFFIC_POLICE' THEN
        INSERT INTO TRAFFIC_POLICE_OFFICER (police_officer_id, user_id, badge_number, rank, station_name) VALUES (POLICE_SEQ.NEXTVAL, v_user_id, p_badge, p_rank, p_station);
    END IF;
    COMMIT;
END;
/

create or replace NONEDITIONABLE PROCEDURE SP_EVALUATE_PRACTICAL_TEST (
    p_application_id IN NUMBER,
    p_officer_id     IN NUMBER,
    p_parking_score  IN NUMBER,
    p_lane_score     IN NUMBER,
    p_sign_score     IN NUMBER,
    p_total_score    IN NUMBER
) AS
    v_status       VARCHAR2(20);
    v_user_id      NUMBER;
    v_challan_no   VARCHAR2(50);
BEGIN
    IF p_total_score >= 70 THEN v_status := 'PASSED'; ELSE v_status := 'FAILED'; END IF;

    UPDATE DRIVING_TEST_SCHEDULE 
    SET status = 'COMPLETED', examiner_officer_id = p_officer_id 
    WHERE application_id = p_application_id;

    -- Intentionally omitting ID here to prevent ORA-32795
    INSERT INTO PRACTICAL_TEST_RESULT (
        application_id, parking_score, lane_score, sign_score, total_score, status, evaluation_date
    ) VALUES (
        p_application_id, p_parking_score, p_lane_score, p_sign_score, p_total_score, v_status, SYSDATE
    );

    IF v_status = 'PASSED' THEN
        UPDATE APPLICATION SET current_stage = 'LICENSE_PAYMENT_PENDING' WHERE application_id = p_application_id;

        SELECT a.user_id INTO v_user_id FROM APPLICATION app JOIN APPLICANT a ON app.applicant_id = a.applicant_id WHERE app.application_id = p_application_id;

        v_challan_no := 'CHL-' || TO_CHAR(SYSDATE, 'YYYY') || '-LIC-' || p_application_id;

        INSERT INTO PAYMENT (payment_id, application_id, user_id, challan_no, amount, payment_type, payment_status)
        VALUES (PAYMENT_SEQ.NEXTVAL, p_application_id, v_user_id, v_challan_no, 2500.0, 'LICENSE_FEE', 'PENDING_CHALLAN');
    ELSE
        UPDATE APPLICATION SET current_stage = 'PRACTICAL_PENDING' WHERE application_id = p_application_id;
    END IF;

    COMMIT;
END;
/

create or replace NONEDITIONABLE PROCEDURE SP_ISSUE_LICENSE (
    p_app_id     IN NUMBER,
    p_officer_id IN NUMBER
) AS
    v_license_count NUMBER;
    v_applicant_id  NUMBER;
    v_category      VARCHAR2(20);
    v_license_no    VARCHAR2(50);
    v_qr_data       VARCHAR2(50);
BEGIN
    SELECT COUNT(*) INTO v_license_count FROM DRIVING_LICENSE WHERE application_id = p_app_id;
    IF v_license_count = 0 THEN
        SELECT applicant_id, license_category INTO v_applicant_id, v_category FROM APPLICATION WHERE application_id = p_app_id;

        v_license_no := 'PB-' || TO_CHAR(SYSDATE, 'YYYY') || '-' || p_app_id;
        v_qr_data := 'DLMS-' || DBMS_RANDOM.STRING('U', 8);

        INSERT INTO DRIVING_LICENSE (license_id, license_number, application_id, applicant_id, category, officer_id, status, issue_date, expiry_date, qr_code_data, total_penalty_points) 
        VALUES (LICENSE_SEQ.NEXTVAL, v_license_no, p_app_id, v_applicant_id, v_category, p_officer_id, 'ACTIVE', SYSDATE, ADD_MONTHS(SYSDATE, 60), v_qr_data, 0);

        UPDATE APPLICATION SET current_stage = 'COMPLETED' WHERE application_id = p_app_id;
    END IF;
    COMMIT;
END;
/

