create or replace NONEDITIONABLE PROCEDURE SP_PROCESS_PAYMENT_DECISION (
    p_payment_id   IN NUMBER,
    p_officer_id   IN NUMBER,
    p_action       IN VARCHAR2, 
    p_remarks      IN VARCHAR2
) AS
BEGIN
    UPDATE PAYMENT 
    SET payment_status = p_action, 
        reviewed_by_officer_id = p_officer_id, 
        remarks = p_remarks
    WHERE payment_id = p_payment_id;

    INSERT INTO PAYMENT_AUDIT_LOG (
        audit_id, payment_id, officer_id, action_taken, remarks, timestamp
    ) VALUES (
        AUDIT_SEQ.NEXTVAL, p_payment_id, p_officer_id, p_action, p_remarks, SYSDATE
    );
    COMMIT;
END;
