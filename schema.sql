create or replace NONEDITIONABLE PROCEDURE SP_UPDATE_STAFF (
    p_user_id       IN NUMBER,
    p_full_name     IN VARCHAR2,
    p_email         IN VARCHAR2,
    p_role          IN VARCHAR2,
    p_password_hash IN VARCHAR2
) AS
    v_email_count NUMBER;
BEGIN
    -- Check if email is taken by someone else
    SELECT COUNT(*) INTO v_email_count FROM DLMS_USER 
    WHERE email = p_email AND user_id != p_user_id;

    IF v_email_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Email is already taken by another user.');
    END IF;

    -- Update basic details
    UPDATE DLMS_USER 
    SET full_name_en = p_full_name, email = p_email, role = p_role 
    WHERE user_id = p_user_id;

    -- Update password only if provided
    IF p_password_hash IS NOT NULL THEN
        UPDATE DLMS_USER SET password_hash = p_password_hash WHERE user_id = p_user_id;
    END IF;

    COMMIT;
END;
