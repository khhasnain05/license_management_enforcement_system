create or replace NONEDITIONABLE TRIGGER TRG_FORMAT_VIOLATION
BEFORE INSERT OR UPDATE ON TRAFFIC_VIOLATION
FOR EACH ROW
BEGIN
    IF :NEW.vehicle_reg_no IS NOT NULL THEN
        :NEW.vehicle_reg_no := UPPER(:NEW.vehicle_reg_no);
    END IF;
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_LOCK_PAID_CHALLAN
BEFORE UPDATE ON TRAFFIC_VIOLATION
FOR EACH ROW
BEGIN
    -- If the challan is PAID, block anyone trying to alter the fine amount.
    IF :OLD.challan_status = 'PAID' AND :NEW.fine_amount != :OLD.fine_amount THEN
        RAISE_APPLICATION_ERROR(-20010, 'Security Error: Cannot modify the fine amount of a PAID challan.');
    END IF;
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_PROTECT_AUDIT_LOG
BEFORE DELETE ON PAYMENT_AUDIT_LOG
FOR EACH ROW
BEGIN
    RAISE_APPLICATION_ERROR(-20013, 'Compliance Error: Deletion of Payment Audit Logs is strictly prohibited.');
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_VALIDATE_FINES
BEFORE INSERT OR UPDATE ON TRAFFIC_VIOLATION
FOR EACH ROW
BEGIN
    -- Prevent negative fine amounts
    IF :NEW.fine_amount < 0 THEN
        RAISE_APPLICATION_ERROR(-20017, 'Database Error: Fine amount cannot be negative.');
    END IF;

    -- Prevent negative penalty points
    IF :NEW.penalty_points < 0 THEN
        RAISE_APPLICATION_ERROR(-20018, 'Database Error: Penalty points cannot be negative.');
    END IF;
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_VALIDATE_LICENSE_DATES
BEFORE INSERT OR UPDATE ON DRIVING_LICENSE
FOR EACH ROW
BEGIN
    -- Ensure expiry date is always after the issue date
    IF :NEW.expiry_date <= :NEW.issue_date THEN
        RAISE_APPLICATION_ERROR(-20015, 'Database Error: License expiry date must be after the issue date.');
    END IF;

    -- If a license is suspended, ensure the suspension end date is actually set
    IF :NEW.status = 'SUSPENDED' AND :NEW.suspension_end_date IS NULL THEN
        RAISE_APPLICATION_ERROR(-20016, 'Database Error: A suspended license must have a suspension end date.');
    END IF;
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_VALIDATE_PERMIT_DATES
BEFORE INSERT OR UPDATE ON LEARNER_PERMIT
FOR EACH ROW
BEGIN
    IF :NEW.expiry_date <= :NEW.issue_date THEN
        RAISE_APPLICATION_ERROR(-20011, 'Data Integrity Error: Expiry date must be strictly after the Issue date.');
    END IF;
END;
/

create or replace NONEDITIONABLE TRIGGER TRG_VALIDATE_TEST_DATE
BEFORE INSERT OR UPDATE ON DRIVING_TEST_SCHEDULE
