create or replace NONEDITIONABLE PROCEDURE SP_SCHEDULE_TEST (
    p_application_id IN NUMBER,
    p_test_center    IN VARCHAR2,
    p_test_date      IN DATE
) AS
    v_daily_count NUMBER;
BEGIN
    -- Allow today or future dates. Reject only past dates.
    IF TRUNC(p_test_date) < TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20002, 'Test date cannot be in the past.');
    END IF;

    -- Check Capacity (30 limit)
    SELECT COUNT(*) INTO v_daily_count 
    FROM DRIVING_TEST_SCHEDULE
    WHERE TRUNC(scheduled_date) = TRUNC(p_test_date) 
    AND test_center = p_test_center
    AND status = 'SCHEDULED';

    IF v_daily_count >= 30 THEN 
        RAISE_APPLICATION_ERROR(-20001, 'Center is fully booked for this date.'); 
    END IF;

    -- Insert new schedule (NO ID COLUMN included)
    INSERT INTO DRIVING_TEST_SCHEDULE (application_id, test_center, scheduled_date, status) 
    VALUES (p_application_id, p_test_center, TRUNC(p_test_date), 'SCHEDULED');

    COMMIT;
END;
