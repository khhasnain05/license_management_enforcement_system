create or replace NONEDITIONABLE PROCEDURE SP_AUTO_UNBLOCK_LICENSE (p_license_id IN NUMBER) AS
BEGIN
    UPDATE DRIVING_LICENSE
    SET status = 'ACTIVE', total_penalty_points = 0, suspension_end_date = NULL
    WHERE license_id = p_license_id 
      AND status = 'SUSPENDED' 
      AND suspension_end_date < SYSDATE;
    COMMIT;
END;
