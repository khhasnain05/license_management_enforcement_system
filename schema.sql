CREATE OR REPLACE FORCE NONEDITIONABLE VIEW V_GLOBAL_OFFICER_AUDIT ("OFFICER_ID", "MODULE", "ACTION_TYPE", "DESCRIPTION", "ACTION_DATE") AS 
SELECT 
    officer_id,
    'FINANCE' AS module,
    action_taken AS action_type,
    'Processed Payment ID: ' || payment_id || ' | Remarks: ' || remarks AS description,
    timestamp AS action_date
FROM PAYMENT_AUDIT_LOG
WHERE officer_id IS NOT NULL

UNION ALL

-- 2. Medical Officer Actions
SELECT 
    test_officer_id AS officer_id,
    'MEDICAL' AS module,
    'CERT_' || status AS action_type,
    'Reviewed Medical Cert ID: ' || certificate_id || ' | Status set to: ' || status AS description,
    approval_date AS action_date
FROM MEDICAL_CERTIFICATE
WHERE test_officer_id IS NOT NULL AND approval_date IS NOT NULL

UNION ALL

-- 3. Testing Examiner Actions (Practical Tests)
SELECT 
    examiner_officer_id AS officer_id,
    'TESTING' AS module,
    'TEST_' || status AS action_type,
    'Evaluated Practical Test for App ID: ' || application_id || ' | Result: ' || status AS description,
    scheduled_date AS action_date
FROM DRIVING_TEST_SCHEDULE
WHERE examiner_officer_id IS NOT NULL AND status IN ('COMPLETED', 'PASSED', 'FAILED')

UNION ALL

-- 4. Traffic Police Actions (Enforcement)
SELECT 
    police_officer_id AS officer_id,
    'ENFORCEMENT' AS module,
    'CHALLAN_ISSUED' AS action_type,
    'Issued Challan: ' || challan_no || ' for ' || REPLACE(violation_type, '_', ' ') || ' (Fine: PKR ' || fine_amount || ')' AS description,
    violation_date AS action_date
FROM TRAFFIC_VIOLATION
WHERE police_officer_id IS NOT NULL;


CREATE OR REPLACE FORCE NONEDITIONABLE VIEW VW_APPLICANT_DASHBOARD ("CNIC", "FULL_NAME_EN", "DISTRICT", "APPLICATION_ID", "LICENSE_CATEGORY", "APPLICATION_TYPE", "APPLICATION_STATUS", "CURRENT_STAGE", "LEARNER_PERMIT_NO", "LEARNER_EXPIRY", "LICENSE_NUMBER", "LICENSE_STATUS") AS 
SELECT 
    u.cnic,
    u.full_name_en,
    u.district,
    app.application_id,
    app.license_category,
    app.application_type,
    app.application_status,
    app.current_stage,
    lp.learner_permit_no,
    lp.expiry_date AS learner_expiry,
    dl.license_number,
    dl.status AS license_status
FROM APPLICANT a
JOIN DLMS_USER u ON a.user_id = u.user_id
JOIN APPLICATION app ON a.applicant_id = app.applicant_id
LEFT JOIN LEARNER_PERMIT lp ON app.application_id = lp.application_id
LEFT JOIN DRIVING_LICENSE dl ON app.application_id = dl.application_id;


CREATE OR REPLACE FORCE NONEDITIONABLE VIEW VW_CENTER_PERFORMANCE ("TEST_CENTER", "TOTAL_TESTS_CONDUCTED", "TOTAL_PASSED", "TOTAL_FAILED", "PASS_RATE_PERCENTAGE") AS 
SELECT 
    s.test_center,
    COUNT(r.id) AS total_tests_conducted,
    SUM(CASE WHEN r.status = 'PASSED' THEN 1 ELSE 0 END) AS total_passed,
    SUM(CASE WHEN r.status = 'FAILED' THEN 1 ELSE 0 END) AS total_failed,
    ROUND((SUM(CASE WHEN r.status = 'PASSED' THEN 1 ELSE 0 END) / NULLIF(COUNT(r.id), 0)) * 100, 2) AS pass_rate_percentage
FROM driving_test_schedule s
JOIN practical_test_result r ON s.application_id = r.application_id
GROUP BY s.test_center;


